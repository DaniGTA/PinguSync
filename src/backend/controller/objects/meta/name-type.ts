export enum NameType {
    /**
     * Main or Original Name of a Series
     */
    MAIN = 'main',
    /**
     * A Official Name but not the original one (Translated Name)
     */
    OFFICIAL = 'official',
    /**
     * A short Name version
     */
    SHORT = 'short',
    SYN = 'syn',
    SLUG = 'slug',
    UNKNOWN = 'unkown',
}
